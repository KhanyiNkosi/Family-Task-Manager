-- ============================================================================
-- INSPECT REWARDS TABLE CONSTRAINTS AND TRIGGERS
-- ============================================================================
-- This will show us what's preventing clean cascade deletes

-- 1. Show all constraints on rewards table
SELECT
  tc.constraint_name,
  tc.constraint_type,
  kcu.column_name,
  ccu.table_name AS foreign_table_name,
  ccu.column_name AS foreign_column_name,
  rc.update_rule,
  rc.delete_rule
FROM information_schema.table_constraints AS tc
JOIN information_schema.key_column_usage AS kcu
  ON tc.constraint_name = kcu.constraint_name
  AND tc.table_schema = kcu.table_schema
LEFT JOIN information_schema.constraint_column_usage AS ccu
  ON ccu.constraint_name = tc.constraint_name
  AND ccu.table_schema = tc.table_schema
LEFT JOIN information_schema.referential_constraints AS rc
  ON rc.constraint_name = tc.constraint_name
WHERE tc.table_name = 'rewards'
ORDER BY tc.constraint_type, tc.constraint_name;

-- 2. Show triggers on families table that might affect rewards
SELECT 
  trigger_name,
  event_manipulation,
  event_object_table,
  action_statement,
  action_timing
FROM information_schema.triggers
WHERE event_object_table = 'families'
ORDER BY trigger_name;

-- 3. Show triggers on rewards table
SELECT 
  trigger_name,
  event_manipulation,
  event_object_table,
  action_statement,
  action_timing
FROM information_schema.triggers
WHERE event_object_table = 'rewards'
ORDER BY trigger_name;

-- 4. Get the actual trigger function code (if exists)
SELECT 
  p.proname AS function_name,
  pg_get_functiondef(p.oid) AS function_definition
FROM pg_proc p
JOIN pg_namespace n ON p.pronamespace = n.oid
WHERE n.nspname = 'public'
  AND p.proname LIKE '%family%delete%'
  OR p.proname LIKE '%reward%'
ORDER BY p.proname;
